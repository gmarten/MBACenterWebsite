	                                <h3>Tell us about your experience working for the Prime Minister’s office... </h3>
                                    <p> 
                                    I have always been extremely interested in politics and political philosophy, but I had never really considered taking a job in that field.
                                    My main interest was research and academia. So, when I was asked to join Dominique de Villepin’s staff, it was quite a surprise for me.
                                    I did not hesitate very long before accepting the position. I worked for former Prime Minister Dominique de Villepin from 2005 to the most
                                    recent election. It was a great opportunity to act in the forefront and to make a difference. And it was also the chance to work for a man
                                    of great convictions and tireless energy. I first discovered Dominique de Villepin when he delivered his famous 2003 speech at the United
                                    Nations Security Council, and I have deeply admired him since then.
                                    </p>
                                    
                                    <p>
	                                   Despite my enthusiasm and good will, my first weeks at the Prime Minister’s Office were really tough. I had not studied political
	                                   science and had never worked in a government department, and my knowledge of the legislative process was somewhat limited. But I spent
	                                   extra time studying constitutional and administrative law and learned from my new colleagues. Soon enough, my efforts paid off and a few
	                                   months after starting, the chief of staff gave me my first important speech to write. 

                                    </p>
                                    
                                    <h3>Why did you choose to do an MBA at this point in your career ?</h3>
                                    
                                    <p>
                                      I want to design a career that combines my interest in science and research with my taste for entrepreneurship. My long-term goal is to
                                      start up a biotech company. To achieve this goal, I need first to acquire the business tools I currently lack. I believe Harvard is the
                                      best place for me to do so. Harvard’s general management focus will give me a broad perspective on all aspects of running a business. It
                                      will teach me how to handle the countless issues managers must face in a world of fierce commercial competition. I also feel that the
                                      case-study method is the best way for me to acquire these skills. I can study the theory in textbooks by myself, but I cannot discover
                                      alone the many realities of the business world. 
                                    </p>
                                    <h3>What was the hardest part of the application process? </h3>
                                     
                                    <p>
                                     Writing the essays was difficult. In one question, for example, I was asked to discuss an experience that highlighted my strengths and
                                     weaknesses as a leader. I decided to talk about a particularly gifted student of mine whose behavior in class was preventing me from
                                     seeing the larger picture. All the other students were feeling left behind. In turning that situation around, I realized that the role of
                                     a leader is about developing everyone’s potential to the fullest and setting the right dynamics for the group as a whole. Another
                                     difficult element in the application process was the interview, but for that part of the application, I relied on the help of MBA
                                     application professionals.
                                     </p>
                                     
                                     <h3>What advice would you give to other MBA applicants? </h3>
                                    <p>I would urge them to focus on the essays and not to worry about topping up the GMAT scores. Of course you need to have a good score, but 
                                    10 or 20 points will not make that much of a difference. It is the essays that count; this is where you can really give a sense of who you
                                     are as a person and why you want to pursue an MBA. The time you spend with your MBA coach discussing what your values are and what you 
                                     want to do with your life is time well spent. Also, if you are married as I am, be sure to involve your partner in the process. My wife
                                      (she recently gave birth to a wonderful baby boy) was very enthusiastic and is now in touch with other MBA partners at Harvard.</p>
